####################  CALCULO DERECHOS SEXUALES Y REPRODUCTIVOS

LOGRO16 <- DATA[,c("A01","IdIntegrante","E02_1","F23")]

lazy_dt(LOGRO16)

LOGRO16 <- sqldf("SELECT *, 
            CASE WHEN E02_1 < 12 THEN 'NO APLICA'
                 WHEN E02_1 >= 12 AND F23 IS NULL THEN 'SIN DATO'
                 WHEN E02_1 >= 12 AND F23 = '1'  THEN 'ALCANZADO' ELSE 'POR ALCANZAR' END ESTADO_I
            FROM LOGRO16")

#CALCULAR HOGARES CON AL MENOS UNA PERSONA POR ALCANZAR
TMP <-data.frame(unique(LOGRO16[LOGRO16$ESTADO_I=="POR ALCANZAR",c(1)]))
colnames(TMP) <- c("A01")

#CALCULAR HOGARES CON AL MENOS UNA PERSONA ALCAZADO
TMP1 <-data.frame(unique(LOGRO16[LOGRO16$ESTADO_I=="ALCANZADO",c(1)]))
colnames(TMP1) <- c("A01")

#CALCULAR HOGARES CON AL MENOS UNA PERSONA SIN DATO
TMPSD <-data.frame(unique(LOGRO16[LOGRO16$ESTADO_I=="SIN DATO",c(1)]))
colnames(TMPSD) <- c("A01")

#CALCULO LOGRO FAMILIAR
LOGRO16 <- sqldf("SELECT LOGRO16.*, 
            CASE 
            WHEN TMPSD.A01 IS NOT NULL THEN 'SIN DATO'
            WHEN TMP.A01 IS NULL AND TMP1.A01 IS NULL THEN 'NO APLICA' 
            WHEN TMP.A01 IS NULL THEN 'ALCANZADO' ELSE 'POR ALCANZAR' END ESTADO_F
            FROM LOGRO16
            LEFT JOIN TMP ON LOGRO16.A01 = TMP.A01
            LEFT JOIN TMP1 ON LOGRO16.A01 = TMP1.A01
            LEFT JOIN TMPSD ON LOGRO16.A01 = TMPSD.A01")

rm(list = ls()[ls() %in% grep("^TMP",ls(),value = TRUE)])